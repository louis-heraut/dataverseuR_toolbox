META$dsDescriptionValue = "Complète <a href="https://doi.org/10.57745/QDCSBZ"><b>Analyse des débits et de la recharge potentielle des aquifères par niveau de réchauffement et par secteur hydrographique — Fiche de synthèse</b></a>

<hr>

Les <b>projections de débit</b> et de <b>recharge</b> du projet Explore2 ont été examinées par <b>niveau de réchauffement</b> en réponse au besoin d’accompagnement de la trajectoire de réchauffement de référence pour l’adaptation au changement climatique (TRACC).<br><br>

Pour les besoins de diffusion des résultats de ces analyses, des <b>fiches de synthèse</b> ont toutes été élaborées. Elles sont construites sur un schéma identique et associent représentations graphiques et tableaux. Elles décrivent les évolutions à l’échelle de <b>secteur hydrographique</b> (découpage en 187 bassins ou sous-bassins versants). Ces fiches exploitent les résultats aux points de simulation du projet Explore2 disposant de projections de débit issues d’<b>au moins quatre modèles hydrologiques de surface</b> et sur les masses d’eau souterraine associées à des aquifères. L’objectif visé des fiches est de <b>montrer l’évolution pour quelques variables clefs</b> descriptives du climat, de la recharge potentielle des aquifères et de l’hydrologie de surface par niveau de réchauffement et de sensibiliser aux incertitudes associées. Elles s'adressent à l'ensemble des <b>acteurs locaux</b>, et visent à leur apporter la connaissance des <b>grandes tendances</b> que connaîtront les secteurs hydrographiques dans lesquels elles s'inscrivent, pour l'élaboration de leurs documents de <b>planification locale</b>."
